package com.ailuoku6.golib.Model;

public class Guancang {
    private String suoshuhao;
    private String guancangdi;
    private String State;

    public String getSuoshuhao() {
        return suoshuhao;
    }

    public String getGuancangdi() {
        return guancangdi;
    }

    public String getState() {
        return State;
    }

    public void setSuoshuhao(String suoshuhao) {
        this.suoshuhao = suoshuhao;
    }

    public void setGuancangdi(String guancangdi) {
        this.guancangdi = guancangdi;
    }

    public void setState(String state) {
        State = state;
    }
}
